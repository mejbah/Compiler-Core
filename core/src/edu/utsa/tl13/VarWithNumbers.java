package edu.utsa.tl13;

public class VarWithNumbers {
	String name;
	int number; //subscript
	
	public VarWithNumbers(String name, int number) {
		super();
		this.name = name;
		this.number = number;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getNumber() {
		return number;
	}

	public void setNumber(int number) {
		this.number = number;
	}
	
	
	
	

}
